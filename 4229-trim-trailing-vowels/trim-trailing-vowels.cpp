class Solution {
public:
    string trimTrailingVowels(string s) {
        string v="aeiou";
        int n=s.size();
        for(int i=n-1;i>=0;i--){
            if(s[i]=='a' || s[i]=='e' || s[i]=='i' ||s[i]=='o' || s[i]=='u'){
                s.erase(i,1);
            }
            else break;
        }
        return s;
    }
};